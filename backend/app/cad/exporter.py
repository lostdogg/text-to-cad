    @staticmethod
    def generate_qc_report(
        mesh: trimesh.Trimesh,
        validation_result: ValidationResult,
    ) -> Dict[str, Any]:
        """Generate a quality control report dict."""
        bounds = mesh.bounds if mesh.bounds is not None else np.zeros((2, 3))
        size = (bounds[1] - bounds[0]).tolist() if mesh.bounds is not None else [0, 0, 0]
        return {
            "report_type": "QC",
            "generated_at": datetime.utcnow().isoformat(),
            "mesh_stats": {
                "vertex_count": len(mesh.vertices),
                "face_count": len(mesh.faces),
                "volume_mm3": float(mesh.volume) if mesh.is_volume else None,
                "surface_area_mm2": float(mesh.area),
                "bounding_box_mm": {
                    "x": round(size[0], 3),
                    "y": round(size[1], 3),
                    "z": round(size[2], 3),
                },
                "is_watertight": mesh.is_watertight,
                "is_manifold": mesh.is_volume,
            },
            "validation": {
                "is_valid": validation_result.is_valid,
                "error_count": validation_result.error_count(),
                "warning_count": validation_result.warning_count(),
                "issues": [i.dict() for i in validation_result.issues],
            },
            "pass_fail": "PASS" if validation_result.is_valid else "FAIL",
        }

