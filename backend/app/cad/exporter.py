    # G-code exports                                                       #
    # ------------------------------------------------------------------ #

    @staticmethod
    def export_gcode_cnc(
        mesh: trimesh.Trimesh,
        filepath: str,
        params: Optional[CNCParams] = None,
    ) -> str:
        """Generate simplified 3-axis CNC G-code."""
        if params is None:
            params = CNCParams()
        filepath = CADExporter._validate_filepath(filepath)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        bounds = mesh.bounds if mesh.bounds is not None else np.zeros((2, 3))
        min_b, max_b = bounds[0], bounds[1]
        size = max_b - min_b
        lines = [
            f"; CNC G-code generated by Text-to-CAD",
            f"; Material: {params.material}",
            f"; Tool diameter: {params.tool_diameter}mm",
            f"; Spindle: {params.spindle_speed} RPM  Feed: {params.feed_rate} mm/min",
            f"; Bounding box: X[{min_b[0]:.3f},{max_b[0]:.3f}] Y[{min_b[1]:.3f},{max_b[1]:.3f}] Z[{min_b[2]:.3f},{max_b[2]:.3f}]",
            "G21         ; metric units",
            "G90         ; absolute positioning",
            "G17         ; XY plane",
            f"S{params.spindle_speed} M3  ; spindle on",
            "G0 Z5       ; rapid to safe height",
            "G0 X0 Y0    ; rapid to origin",
            "",
            "; ----- Facing pass -----",
        ]
        # Simple facing pass
        step = params.tool_diameter * 0.8
        y = min_b[1]
        z_top = max_b[2] + params.stock_allowance
        lines.append(f"G0 Z{z_top:.3f}")
        lines.append(f"G1 Z{max_b[2]:.3f} F{params.feed_rate * 0.5:.0f}")
        while y <= max_b[1] + step:
            lines.append(f"G1 X{max_b[0]:.3f} Y{y:.3f} F{params.feed_rate:.0f}")
            y += step
            lines.append(f"G1 X{min_b[0]:.3f} Y{y:.3f}")
            y += step
        lines += [
            "",
            "; ----- Contouring pass -----",
            f"G0 Z{z_top:.3f}",
            f"G1 Z{min_b[2]:.3f} F{params.feed_rate * 0.3:.0f}",
            f"G1 X{min_b[0]:.3f} Y{min_b[1]:.3f} F{params.feed_rate:.0f}",
            f"G1 X{max_b[0]:.3f} Y{min_b[1]:.3f}",
            f"G1 X{max_b[0]:.3f} Y{max_b[1]:.3f}",
            f"G1 X{min_b[0]:.3f} Y{max_b[1]:.3f}",
            f"G1 X{min_b[0]:.3f} Y{min_b[1]:.3f}",
            "G0 Z10      ; retract",
            "M5          ; spindle off",
            "M30         ; program end",
        ]
        with open(filepath, "w") as fh:
            fh.write("\n".join(lines))
        return filepath

    @staticmethod
    def export_gcode_3dprint(
        mesh: trimesh.Trimesh,
        filepath: str,
        params: Optional[PrintParams] = None,
    ) -> str:
        """Generate simplified FDM 3D-printing G-code (RepRap/Marlin compatible)."""
        if params is None:
            params = PrintParams()
        filepath = CADExporter._validate_filepath(filepath)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        bounds = mesh.bounds if mesh.bounds is not None else np.zeros((2, 3))
        min_b, max_b = bounds[0], bounds[1]
        height = max_b[2] - min_b[2]
        num_layers = max(1, int(math.ceil(height / params.layer_height)))
        lines = [
            f"; 3D Print G-code generated by Text-to-CAD",
            f"; Printer: {params.printer_type}  Material: {params.material}",
            f"; Layer height: {params.layer_height}mm  Infill: {params.infill_percent}%",
            f"; Layers: {num_layers}",
            "M104 S{nozzle} ; set nozzle temp".format(nozzle=int(params.nozzle_temperature)),
            "M140 S{bed}    ; set bed temp".format(bed=int(params.bed_temperature)),
            "M109 S{nozzle} ; wait nozzle".format(nozzle=int(params.nozzle_temperature)),
            "M190 S{bed}    ; wait bed".format(bed=int(params.bed_temperature)),
            "G28             ; home all",
            "G21             ; metric",
            "G90             ; absolute",
            "M82             ; absolute extruder",
            "G1 Z0.3 F5000  ; raise nozzle",
        ]
        e = 0.0
        for layer in range(num_layers):
            z = min_b[2] + (layer + 0.5) * params.layer_height
            lines.append(f"\n; Layer {layer + 1}/{num_layers}  Z={z:.3f}")
            lines.append(f"G0 Z{z:.3f} F5000")
            # Simple perimeter trace
            cx = (min_b[0] + max_b[0]) / 2
            cy = (min_b[1] + max_b[1]) / 2
            rx = (max_b[0] - min_b[0]) / 2
            ry = (max_b[1] - min_b[1]) / 2
            perimeter = 2 * (rx * 2 + ry * 2)
            e += perimeter * params.layer_height * params.nozzle_diameter / (math.pi * 0.875)
            lines.append(f"G1 X{min_b[0]:.2f} Y{min_b[1]:.2f} F{params.print_speed * 60:.0f}")
            lines.append(f"G1 X{max_b[0]:.2f} Y{min_b[1]:.2f} E{e:.4f}")
            lines.append(f"G1 X{max_b[0]:.2f} Y{max_b[1]:.2f} E{e + perimeter * 0.25:.4f}")
            e += perimeter * 0.5
            lines.append(f"G1 X{min_b[0]:.2f} Y{max_b[1]:.2f} E{e:.4f}")
            lines.append(f"G1 X{min_b[0]:.2f} Y{min_b[1]:.2f} E{e + perimeter * 0.25:.4f}")
            e += perimeter * 0.25
        lines += [
            "\n; End of print",
            "M104 S0  ; nozzle off",
            "M140 S0  ; bed off",
            "M84      ; motors off",
        ]
        with open(filepath, "w") as fh:
            fh.write("\n".join(lines))
        return filepath

    @staticmethod
    def export_gcode_laser(
        profile: List[List[float]],
        filepath: str,
        params: Optional[LaserParams] = None,
    ) -> str:
        """Generate laser cutting G-code for a 2-D profile (list of [x,y] points)."""
        if params is None:
            params = LaserParams()
        filepath = CADExporter._validate_filepath(filepath)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        speed_mm_min = params.speed * 60
        lines = [
            f"; Laser cut G-code generated by Text-to-CAD",
            f"; Material: {params.material}  Thickness: {params.sheet_thickness}mm",
            f"; Power: {params.power}%  Speed: {params.speed}mm/s  Kerf: {params.kerf_width}mm",
            "G21          ; metric",
            "G90          ; absolute",
            "G0 X0 Y0     ; home",
            f"M3 S0        ; laser armed (off)",
        ]
        for _pass in range(params.passes):
            lines.append(f"\n; Pass {_pass + 1}/{params.passes}")
            if profile:
                sx, sy = profile[0]
                lines.append(f"G0 X{sx:.3f} Y{sy:.3f}   ; move to start")
                lines.append(f"M3 S{int(params.power * 10)}  ; laser on")
                for px, py in profile[1:]:
                    lines.append(f"G1 X{px:.3f} Y{py:.3f} F{speed_mm_min:.0f}")
                lines.append(f"G1 X{sx:.3f} Y{sy:.3f} F{speed_mm_min:.0f}  ; close path")
                lines.append("M5 S0        ; laser off")
        lines += ["G0 X0 Y0     ; return home", "M2           ; program end"]
        with open(filepath, "w") as fh:
            fh.write("\n".join(lines))
        return filepath

