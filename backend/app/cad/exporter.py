"""CAD file exporter: STL, OBJ, simplified STEP, G-code (CNC, 3D print, laser).

Also generates QC reports and procurement specification dicts.
"""

from __future__ import annotations

import logging
import math
import os
import re
from datetime import datetime
from typing import Any, Dict, List, Optional

import numpy as np
import trimesh

from ..models.manufacturing import (
    CNCParams,
    LaserParams,
    PrintParams,
    ValidationResult,
)

logger = logging.getLogger(__name__)


class CADExporter:
    """Export meshes to various formats and generate reports."""

    @staticmethod
    def _validate_filepath(filepath: str) -> str:
        """Resolve and validate filepath to prevent path traversal attacks."""
        resolved = os.path.realpath(os.path.abspath(filepath))
        # Must be an absolute path with no null bytes
        if "\x00" in filepath:
            raise ValueError("Filepath contains null bytes")
        # Reject paths with suspicious traversal patterns before resolution
        if re.search(r"\.\.[/\\]", filepath):
            raise ValueError(f"Path traversal detected in filepath: {filepath}")
        return resolved

    @staticmethod
    def export_stl(mesh: trimesh.Trimesh, filepath: str, binary: bool = True) -> str:
        """Export mesh to STL file. Returns filepath."""
        filepath = CADExporter._validate_filepath(filepath)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        if binary:
            data = mesh.export(file_type="stl")
        else:
            data = mesh.export(file_type="stl_ascii")
        with open(filepath, "wb") as f:
            f.write(data)
        logger.info("Exported STL -> %s (%d bytes)", filepath, len(data))
        return filepath

    @staticmethod
    def export_obj(mesh: trimesh.Trimesh, filepath: str) -> str:
        """Export mesh to Wavefront OBJ. Returns filepath."""
        filepath = CADExporter._validate_filepath(filepath)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        data = mesh.export(file_type="obj")
        with open(filepath, "wb") as f:
            f.write(data)
        return filepath

    @staticmethod
    def export_step(mesh: trimesh.Trimesh, filepath: str) -> str:
        """Export a simplified STEP-like ASCII representation.

        Note: Full STEP (ISO 10303) requires a dedicated kernel (e.g. OpenCASCADE).
        This produces a human-readable approximation useful for documentation.
        """
        filepath = CADExporter._validate_filepath(filepath)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        now = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%S")
        bounds = mesh.bounds if mesh.bounds is not None else np.zeros((2, 3))
        lines = [
            "ISO-10303-21;",
            "HEADER;",
            f"FILE_DESCRIPTION(('Text-to-CAD Export'),'2;1');",
            f"FILE_NAME('{os.path.basename(filepath)}','{now}',('Text-to-CAD'),(''),",
            "  'Text-to-CAD v1.0','','');",
            "FILE_SCHEMA(('AUTOMOTIVE_DESIGN { 1 0 10303 214 1 1 1 1 }'));",
            "ENDSEC;",
            "DATA;",
            f"/* Bounding box: min={bounds[0].tolist()} max={bounds[1].tolist()} */",
            f"/* Vertices: {len(mesh.vertices)}  Faces: {len(mesh.faces)} */",
            f"/* Volume: {float(mesh.volume):.4f} mm3  Area: {float(mesh.area):.4f} mm2 */",
        ]
        for i, v in enumerate(mesh.vertices):
            lines.append(f"#V{i+1}=CARTESIAN_POINT('',(%.6f,%.6f,%.6f));" % tuple(v))
        for i, f in enumerate(mesh.faces):
            lines.append(f"#F{i+1}=FACE_BOUND('',({f[0]+1},{f[1]+1},{f[2]+1}),.T.);")
        lines += ["ENDSEC;", "END-ISO-10303-21;"]
        with open(filepath, "w") as fh:
            fh.write("\n".join(lines))
        return filepath

